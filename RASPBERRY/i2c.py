import board  # Importa la llibreria board per treballar amb el pinout del microcontrolador
import adafruit_ssd1306  # Importa la llibreria adafruit_ssd1306 per controlar el display OLED
from PIL import Image, ImageDraw, ImageFont  # Importa classes d'imatge de la llibreria Python Imaging Library (PIL)
import textwrap  # Importa la funció textwrap per a ajustar el text
import time  # Importa la llibreria time per treballar amb temps

class Display:
    def __init__(self, address=0x3C, width=128, height=64, font_path='/usr/share/fonts/truetype/dejavu/DejaVuSansMono.ttf', font_size=15):
        self.i2c = board.I2C()  # Inicia la comunicació I2C
        self.oled = adafruit_ssd1306.SSD1306_I2C(width, height, self.i2c, addr=address)  # Configura el display OLED
        self.font_path = font_path  # Guarda la ruta de la font
        self.font_size = font_size  # Guarda la mida de la font
        self.width = width  # Guarda l'amplada del display
        self.height = height  # Guarda l'alçada del display

    def clear_display(self):
        """Neteja el display."""
        self.oled.fill(0)  # Omple el display amb negre
        self.oled.show()  # Mostra el contingut del display

    def draw_text(self, text, font_size=None):
        """Dibuixa text al display."""
        self.clear_display()  # Esborra el contingut actual del display
        if font_size is None:
            font_size = self.font_size  # Usa la mida de la font predeterminada si no s'especifica
        font = ImageFont.truetype(self.font_path, font_size)  # Crea una nova font
        image = Image.new("1", (self.width, self.height))  # Crea una nova imatge
        draw = ImageDraw.Draw(image)  # Inicia el dibuix en la nova imatge
        wrapped_text = textwrap.fill(text, width=14)  # Ajusta el text per a una longitud de 14 caràcters
        lines = wrapped_text.split('\n')  # Separa el text en línies
        y = 0  # Inicialitza la coordenada y
        for line in lines:
            draw.text((0, y), line, font=font, fill=255)  # Dibuixa el text en la imatge
            y += font.getsize(line)[1]  # Augmenta la coordenada y per a la següent línia
        self.oled.image(image)  # Actualitza el contingut del display amb la nova imatge
        self.oled.show()  # Mostra el contingut del display

    def show_image(self, image_path="./foto_capturada.jpg"):
        """Mostra una imatge al display."""
        try:
            image = Image.open(image_path)  # Obre la imatge
            image = image.convert("L")  # Converteix la imatge a escala de grisos
            image = image.resize((self.width, self.height))  # Redimensiona la imatge
            image = image.convert("1")  # Converteix la imatge a blanc i negre
            self.oled.image(image)  # Actualitza el contingut del display amb la nova imatge
            self.oled.show()  # Mostra el contingut del display
        except Exception as e:
            print(f"Error al mostrar la imatge: {e}")  # Imprimeix un missatge d'error si hi ha algun problema

    def draw_bitmap(self, bitmap_bytes):
        """
        Dibuixa un mapa de bits al display a partir d'un array de bytes.
        """
        image = Image.new('1', (self.width, self.height))  # Crea una nova imatge en blanc i negre
        draw = ImageDraw.Draw(image)  # Inicia el dibuix en la nova imatge

        x = 0  # Coordenada x inicial
        y = 0  # Coordenada y inicial

        for byte in bitmap_bytes:  # Itera a través dels bytes del mapa de bits
            for bit in range(8):  # Itera a través dels bits de cada byte
                if byte & (1 << (7-bit)):  # Comprova si el bit actual està encès
                    draw.point((x, y), fill=255)  # Dibuixa un píxel blanc
                x += 1  # Augmenta la coordenada x per al següent bit
                if x >= self.width:  # Comprova si s'ha arribat al final de la fila
                    x = 0  # Reinicia la coordenada x
                    y += 1  # Passa a la següent fila

        self.oled.image(image)  # Actualitza el contingut del display amb la nova imatge
        self.oled.show()  # Mostra el contingut del display

